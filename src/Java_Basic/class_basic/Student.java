package Java_Basic.class_basic;

/**
 * 학생 정보를 담는 클래스
 * 클래스는 객체의 설계도 역할을 하며, 객체의 속성과 행위를 정의합니다.
 * 
 * 클래스 명명 규칙:
 * 1. 대문자로 시작
 * 2. 낙타 표기법 사용 (CamelCase)
 * 3. 명사나 명사구를 사용
 * 4. 의미있고 구체적인 이름 사용
 */
public class Student {

    /**
     * 멤버 변수 (Member Variables) 또는 필드 (Fields)
     * 
     * 멤버 변수와 필드의 의미:
     * - 멤버 변수: 클래스에 소속된 변수를 의미
     * - 필드: 데이터베이스나 엑셀에서의 데이터 항목을 의미하는 전통적인 용어
     * 
     * 변수 명명 규칙:
     * 1. 소문자로 시작
     * 2. 낙타 표기법 사용
     * 3. 의미있고 구체적인 이름 사용
     */
    String name;    // 학생의 이름을 저장하는 필드
    int age;        // 학생의 나이를 저장하는 필드
    int grade;      // 학생의 학년을 저장하는 필드
}
